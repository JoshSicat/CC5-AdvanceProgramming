import math

#ceiling the number 
a = 2.3 
ceil_a = math.ceil(a)
print(f"Ceil of (a) is {ceil_a}")

#the floor of the number
floor_a = math.floor(a)
print(f"floor of {a} is {floor_a}")

#the factorial of the number
b = 5
factorial_b = math.factorial(b)
print(f"factorial of {b} is {factorial_b}")

#absolute value of number
abs_value = abs(23)
print(f"abosulte value of {23} is {abs_value}")

#square root for the numer
c =  16
sqrt_c = math.sqrt(c)
print(f"square root of {c} is {sqrt_c}")